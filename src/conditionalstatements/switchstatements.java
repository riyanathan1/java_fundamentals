package conditionalstatements;


public class switchstatements {
    public static void main(String[] args) {


        int floor = 5;
        switch (floor) {
            case 1:
                System.out.println("this is floor 1");
                break;
            case 2:
                System.out.println("this is floor 2");
                break;
            case 3:
                System.out.println("this is floor 3");
                break;
            case 4:
                System.out.println("this is floor 4");
                break;
            case 5:
                System.out.println("this is floor 5");

        }
    }
}


